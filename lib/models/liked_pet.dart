class LikedPet {
  final String name;
  final String image;
  final int age;
  final String breed;
  final String distance;
  final double weight;
  final String likedDate;
  final String petType;
  final String about;

  LikedPet({
    required this.name,
    required this.image,
    required this.age,
    required this.breed,
    required this.distance,
    required this.weight,
    required this.likedDate,
    required this.petType,
    required this.about,
  });
}
